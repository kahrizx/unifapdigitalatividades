let numero1 = parseFloat(prompt("Digite o primeiro número:"));
let numero2 = parseFloat(prompt("Digite o segundo número (diferente do primeiro):"));
let numero3 = parseFloat(prompt("Digite o terceiro número (diferente dos anteriores):"));
let numero4 = parseFloat(prompt("Digite o quarto número (diferente dos anteriores):"));

let media = (numero1 + numero2 + numero3 + numero4) / 4;

console.log("A média dos números é: " + media);
